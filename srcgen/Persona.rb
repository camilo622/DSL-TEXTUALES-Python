class Persona {
	attr_accessor :nombre, :apellido, :direccion, :edad, :telefono, :correo 
	
	
	def initialize (nombre,apellido,direccion,edad,telefono,correo	)
	
	 
  	    @nombre = nombre
  	    @apellido = apellido
	    @direccion = direccion
  	    @edad = edad
	    @telefono = telefono
  	    @correo = correo
	end
}
end
end



